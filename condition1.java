class condition1
{
 public static void main(String[] args)
 {
 int x = 5;
 if(x>=0 && x<=9)
 {
    System.out.println("x is in 0 to 9 category");
 }   
 else if(x>=10 && x<=19)
 {
    System.out.println("x is in 10 to 19 category");
 }
 else if(x>=20 && x<=29)
{
    System.out.println("x is in 20 to 29 category");
}
else
{
    System.out.println("x is in none of the categories");
}
 }   
}
